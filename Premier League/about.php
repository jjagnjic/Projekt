		<p>The Premier League is the organising body of the Premier League with responsibility for the competition, its Rule Book and the centralised broadcast and other commercial rights.</p>
		<p>The Premier League is a private company wholly owned by its 20 Member Clubs who make up the League at any one time. Each individual club is independent, working within the rules of football, as defined by the Premier League, The FA, UEFA and FIFA, as well being subject to English and European law.</p>
		<p>The Premier League AGM takes place at the close of each season, at which time the relegated clubs transfer their shares to the clubs promoted into the Premier League from the Football League Championship. Clubs have the opportunity to propose new rules or amendments at the Shareholder meeting.</p>
		<p>Each Member Club is entitled to one vote and all rule changes and major commercial contracts require the support of at least a two-thirds vote, or 14 clubs, to be agreed. The Premier League Rule Book serves as a contract between the League, the Member Clubs and one another, defining the structure and running of the competition.</p>
		<p>Any serious breach of the Rule Book results in an independent three-person tribunal sitting to hear the case, ascertain guilt and set the punishment, which can range from fines to points deductions and, in extreme cases, expulsion from the competition (this has never happened in the history of the Premier League).</p>
